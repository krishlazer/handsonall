package space.program;

public class SpaceShuttle 
{

	private String ShuttleID;
	private String ShuttleName;
	private String AssembledStation;
	private int OccupantsCapacity;
	private String FuelType;
	private int FuelCapacity;
	private String Commander;
	private String AsstCommander;
	private String SpaceScientist;
	private int Payload;
	
	public String getShuttleID() 
	{
		return ShuttleID;
	}
	public void setShuttleID(String ShuttleID) 
	{
		this.ShuttleID = ShuttleID;
	}
	public String getShuttleName() 
	{
		return ShuttleName;
	}
	public void setShuttleName(String ShuttleName) 
	{
		this.ShuttleName = ShuttleName;
	}
	public String getAssembledStation() 
	{
		return AssembledStation;
	}
	public void setAssembledStation(String AssembledStation) 
	{
		this.AssembledStation = AssembledStation;
	}
	public int getOccupantsCapacity() 
	{
		return OccupantsCapacity;
	}
	public void setOccupantsCapacity(int OccupantsCapacity) 
	{
		this.OccupantsCapacity = OccupantsCapacity;
	}
	public String getFuelType() 
	{
		return FuelType;
	}
	public void setFuelType(String FuelType) 
	{
		this.FuelType = FuelType;
	}
	public int getFuelCapacity() 
	{
		return FuelCapacity;
	}
	public void setFuelCapacity(int FuelCapacity) 
	{
		this.FuelCapacity = FuelCapacity;
	}
	public String getCommander() 
	{
		return Commander;
	}
	public void setCommander(String Commander) 
	{
		this.Commander = Commander;
	}
	public String getAsstCommander() 
	{
		return AsstCommander;
	}
	public void setAsstCommander(String AsstCommander) 
	{
		this.AsstCommander = AsstCommander;
	}
	public String getSpaceScientist() 
	{
		return SpaceScientist;
	}
	public void setSpaceScientist(String SpaceScientist) 
	{
		this.SpaceScientist = SpaceScientist;
	}
	public int getPayload() 
	{
		return Payload;
	}
	public void setPayload(int Payload) 
	{
		this.Payload = Payload;
	}
}
